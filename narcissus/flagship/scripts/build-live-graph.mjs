// build-live-graph.mjs — the COMPOUNDING wiring. Loads Clotho's live weave snapshot, runs
// Lachesis's measurements and Atropos's supersession verdict over it, and emits ONE
// live-graph.json that the flagship renders. This is where the three modules interoperate
// THROUGH the live graph: Clotho maintains it, Lachesis measures it, Atropos verifies it,
// Narcissus renders the result. Node-only (build step); the browser reads the emitted JSON.
import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { loadWeave } from "../../../lachesis/ingest.mjs";
import { blastRadius, dependencies, relevance, riskClass } from "../../../lachesis/measure.mjs";
import { loadAuthority, verify } from "../../../atropos/verify.mjs";

const HERE = path.dirname(fileURLToPath(import.meta.url));
const REPO = path.resolve(HERE, "../../..");

// --- CLOTHO: the live weave (via Lachesis's pinned view of the current snapshot) ---
const manifest = JSON.parse(readFileSync(path.join(REPO, "lachesis/config/snapshot-manifest.json"), "utf8"));
const weave = loadWeave(manifest, REPO);

// --- LACHESIS: measure the graph. Rank by depends-on in-degree (cheap), measure the top K fully. ---
const indeg = new Map();
for (const e of weave.edges) if (e.edge_kind === "depends-on") indeg.set(e.to, (indeg.get(e.to) || 0) + 1);
const K = 24;
const ranked = [...indeg.entries()].sort((a, b) => b[1] - a[1]).slice(0, K).map(([id]) => id);
// loadWeave's frozen view drops locator payloads; read the raw snapshot for human labels/kinds.
const labels = new Map(), kinds = new Map();
for (const line of readFileSync(path.join(REPO, manifest.snapshot_path), "utf8").split("\n")) {
  if (!line) continue;
  let r; try { r = JSON.parse(line); } catch { continue; }
  if (!r.edge_kind) continue;
  for (const [n, loc] of [[r.from_node, r.from_locator], [r.to_node, r.to_locator]]) {
    if (loc && !labels.has(n)) { labels.set(n, (loc.locator && (loc.locator.symbol || loc.locator.path)) || loc.kind || n.slice(0, 8)); kinds.set(n, loc.kind || null); }
  }
}
const measured = ranked.map((id) => {
  const rc = riskClass(weave, id);
  return {
    id,
    label: labels.get(id) || id.slice(0, 8),
    kind: kinds.get(id) || null,
    dependencies: dependencies(weave, id).size,
    blast_radius: rc.blast_radius,
    relevance: Number(relevance(weave, id).toFixed(4)),
    risk_class: rc.class
  };
});

// --- edges among the measured nodes (a bounded, renderable subgraph) ---
const inSet = new Set(ranked);
const subEdges = [];
const seen = new Set();
for (const e of weave.edges) {
  if (e.edge_kind !== "depends-on" || !inSet.has(e.from) || !inSet.has(e.to)) continue;
  const key = `${e.from}|${e.to}`;
  if (seen.has(key)) continue; seen.add(key);
  subEdges.push({ from: e.from, to: e.to });
}

// --- ATROPOS: verify the supersession the graph's authority tracks ---
const authority = loadAuthority(path.join(REPO, "CURRENT-AUTHORITY.json"));
const verdict = verify(authority, REPO);

const liveGraph = {
  note: "Generated by narcissus/flagship/scripts/build-live-graph.mjs — the live Clotho weave, MEASURED by Lachesis and VERIFIED by Atropos. Regenerate after each re-weave.",
  generated_from_snapshot: manifest.snapshot_digest,
  clotho: {
    total_nodes: weave.nodes.size,
    total_edges: weave.edges.length,
    depends_on_edges: weave.edges.filter((e) => e.edge_kind === "depends-on").length
  },
  measured_by_lachesis: {
    top_by_blast_radius: measured.sort((a, b) => b.blast_radius - a.blast_radius),
    max_blast_radius: Math.max(...measured.map((m) => m.blast_radius))
  },
  subgraph_edges: subEdges,
  verified_by_atropos: {
    verdict: verdict.verdict,
    retired_plan_versions: verdict.advisory.retired_plan_versions,
    active_plan_version: verdict.active_plan_version,
    deferred: verdict.advisory.deferred
  }
};

const OUT = path.join(HERE, "..", "src", "live-graph.json");
writeFileSync(OUT, JSON.stringify(liveGraph, null, 2) + "\n");
console.log(`live-graph.json written: ${weave.nodes.size} nodes / ${weave.edges.length} edges measured; ` +
  `top blast=${liveGraph.measured_by_lachesis.max_blast_radius}; supersession=${verdict.verdict} (${verdict.advisory.retired_plan_versions.length} retired)`);
